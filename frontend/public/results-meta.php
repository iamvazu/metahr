<?php
/**
 * Ee-in Dynamic OG Metadata Gateway
 * Redirects bots/crawlers to specialized metadata for shared results.
 */

$sessionId = $_GET['sessionId'] ?? '';
$isBot = false;

$bots = [
    'facebookexternalhit', 'LinkedInBot', 'WhatsApp', 'Twitterbot', 'Slackbot', 
    'Discordbot', 'TelegramBot', 'Googlebot', 'Bingbot'
];

$userAgent = $_SERVER['HTTP_USER_AGENT'] ?? '';
foreach ($bots as $bot) {
    if (stripos($userAgent, $bot) !== false) {
        $isBot = true;
        break;
    }
}

// Default Meta Data
$title = "MetaHR // Ee-in Digital Twin";
$description = "Upload your leadership reports for a high-precision prescription from Ian Kishander's Digital Twin.";
$image = "https://metahr.co.in/og-preview.png"; // We'll need to ensure this exists

if ($sessionId && $isBot) {
    // Fetch analysis from our API
    $apiUrl = "https://metahr.co.in/wp-json/ee-in/v1/fetch-analysis";
    $ch = curl_init($apiUrl);
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
    curl_setopt($ch, CURLOPT_POST, true);
    curl_setopt($ch, CURLOPT_POSTFIELDS, json_encode(['sessionId' => $sessionId]));
    curl_setopt($ch, CURLOPT_HTTPHEADER, ['Content-Type: application/json']);
    $response = curl_exec($ch);
    $data = json_decode($response, true);
    curl_close($ch);

    if ($data && isset($data['status']) && $data['status'] === 'success') {
        $name = $data['name'] ?? 'Executive';
        $archetype = $data['analysis']['personality_archetype'] ?? 'High-Potential Leader';
        
        $title = "Ee-in Prescription: \"$archetype\"";
        $description = "Leadership Synthesis for $name. Generated by Ee-in, the digital twin of Ian Kishander.";
    }
}

// If it's not a bot, or we don't need special meta, just serve the main app.
// For bots, we output the minimal HTML with meta tags.
if ($isBot) {
?>
<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <title><?php echo $title; ?></title>
    <meta name="description" content="<?php echo $description; ?>">
    
    <!-- Open Graph / Facebook -->
    <meta property="og:type" content="website">
    <meta property="og:url" content="https://metahr.co.in/ee-in/results/<?php echo $sessionId; ?>">
    <meta property="og:title" content="<?php echo $title; ?>">
    <meta property="og:description" content="<?php echo $description; ?>">
    <meta property="og:image" content="<?php echo $image; ?>">

    <!-- Twitter -->
    <meta property="twitter:card" content="summary_large_image">
    <meta property="twitter:url" content="https://metahr.co.in/ee-in/results/<?php echo $sessionId; ?>">
    <meta property="twitter:title" content="<?php echo $title; ?>">
    <meta property="twitter:description" content="<?php echo $description; ?>">
    <meta property="twitter:image" content="<?php echo $image; ?>">
</head>
<body>
    <h1><?php echo $title; ?></h1>
    <p><?php echo $description; ?></p>
</body>
</html>
<?php
    exit;
}

// For real users, just serve index.html
include('index.html');
?>
